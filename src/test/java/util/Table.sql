  id INT PRIMARY KEY NOT NULL,

  namee VARCHAR(50) NOT NULL,

  surname VARCHAR(50) NOT NULL,

  mail VARCHAR(50) NOT NULL,

  pnumber INT,

  pnumber2 INT,

  pass VARCHAR(50) NOT NULL

);